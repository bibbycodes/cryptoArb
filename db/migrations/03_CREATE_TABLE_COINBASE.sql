CREATE TABLE coinbase_ticker (
  id SERIAL PRIMARY KEY,
  exchange VARCHAR(120) NOT NULL,
  timestamp BIGINT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  price NUMERIC(10,4),
  open_24h NUMERIC(10, 4),
  volume_24h NUMERIC(10,4),
  low_24h NUMERIC(10,4),
  high_24h NUMERIC(10,4),
  volume_30d NUMERIC(10,4),
  best_bid NUMERIC(10,4),
  best_ask NUMERIC(10,4),
  side VARCHAR(20),
  trade_id NUMERIC(10,4),
  last_size NUMERIC(10,4),
)